import time

# For loop = execute the block of code for a limited amount of times
#
# While loop = Unlimited
# For loop = limited

# for i in range(start, stop, step):

for i in range(10):
    print(i + 1)

for i in range(50, 100 + 1):
    print(i)

for i in 'SliceOnePiece':
    print(i)

# create a countdown timer

for seconds in range(10,0,-1):
    print(seconds)
    time.sleep(1)





