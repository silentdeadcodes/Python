# 2D lists = A list of lists

drinks = ['soda', 'coffee', 'tea']
dinner = ['pizza', 'burger', 'noodles']
dessert = ['cake', 'ice cream']

food = [drinks,dinner,dessert]


# to print certain list in the list 'print(x[y])'
print(food[0])

# and to access an element which is in a list inside a list
# REMEMBER, Index = from 0 to how much elements you have
print(food[0][0])




