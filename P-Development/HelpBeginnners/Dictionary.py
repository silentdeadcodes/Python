# Dictionary is a key value pair
# 'x = {'key': value}' or 'x['key'] = value' simple line of code for key, also it can be an integer

k = {'key': 2}

# To acces the value:

print(k['key'])

# It can be integer also:

x = {2: 4}
print(x[2])

# We can also see if the value is in the key or dict.

print(2 in x)

# We can also take the values

print(x.values())

# you an also add list cause if dict i a different data type

print(list(x.values()))

# We can also ask for a list of keys:

print(list(x.keys()))



